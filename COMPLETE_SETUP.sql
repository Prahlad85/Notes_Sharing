-- A. CORE TABLES (NOTES & RELATED)

-- 1. Create Notes Table
create table if not exists notes (
  id bigint primary key generated by default as identity,
  created_at timestamp with time zone default timezone('utc'::text, now()) not null,
  subject text not null,
  semester integer not null,
  written_by text not null,
  file_url text not null,
  exam_type text default 'Class Note',
  is_pinned boolean default false,
  user_id uuid references auth.users -- Added for creator tracking
);

-- Ensure columns exist (for migration safety)
do $$
begin
  if not exists (select 1 from information_schema.columns where table_name = 'notes' and column_name = 'exam_type') then
    alter table notes add column exam_type text default 'Class Note';
  end if;
  if not exists (select 1 from information_schema.columns where table_name = 'notes' and column_name = 'user_id') then
    alter table notes add column user_id uuid references auth.users;
  end if;
end $$;

-- 2. Setup User Roles (For Permissions)
create table if not exists user_roles (
  id uuid references auth.users on delete cascade primary key,
  email text,
  role text default 'pending' check (role in ('super_admin', 'admin', 'pending')),
  is_blocked boolean default false,
  created_at timestamp with time zone default timezone('utc'::text, now()) not null
);

-- 3. Setup Notices
create table if not exists notices (
  id bigint primary key generated by default as identity,
  content text not null,
  is_active boolean default true,
  created_at timestamp with time zone default timezone('utc'::text, now()) not null
);


-- B. ROW LEVEL SECURITY (RLS) & POLICIES

-- Enable RLS on all tables
alter table notes enable row level security;
alter table user_roles enable row level security;
alter table notices enable row level security;


-- === NOTES POLICIES ===
drop policy if exists "Enable read access for all users" on notes;
create policy "Enable read access for all users"
  on notes for select using ( true );

drop policy if exists "Enable insert for authenticated users only" on notes;
create policy "Enable insert for authenticated users only"
  on notes for insert with check ( auth.role() = 'authenticated' );

drop policy if exists "Enable delete for authenticated users only" on notes;
-- Updated to allow owners OR super admins to delete
create policy "Enable delete for owners and super admins"
  on notes for delete
  using ( 
    auth.role() = 'authenticated' AND (
       auth.uid() = user_id 
       OR 
       exists (select 1 from user_roles where id = auth.uid() and role = 'super_admin')
    )
  );

drop policy if exists "Enable update for authenticated users only" on notes;
create policy "Enable update for authenticated users only"
  on notes for update using ( auth.role() = 'authenticated' );


-- === USER ROLES POLICIES ===
drop policy if exists "Read own role" on user_roles;
create policy "Read own role" on user_roles 
  for select using (auth.uid() = id);

drop policy if exists "Super admin read all" on user_roles;
create policy "Super admin read all" on user_roles 
  for select using (
    exists (select 1 from user_roles where id = auth.uid() and role = 'super_admin')
  );

drop policy if exists "Super admin update" on user_roles;
create policy "Super admin update" on user_roles 
  for update using (
    exists (select 1 from user_roles where id = auth.uid() and role = 'super_admin')
  );

drop policy if exists "Users can insert self" on user_roles;
create policy "Users can insert self" on user_roles 
  for insert with check (auth.uid() = id);


-- === NOTICES POLICIES ===
drop policy if exists "Public notices are viewable by everyone" on notices;
create policy "Public notices are viewable by everyone"
  on notices for select using ( true );

drop policy if exists "Admins can insert notices" on notices;
create policy "Admins can insert notices"
  on notices for insert with check ( auth.role() = 'authenticated' );

drop policy if exists "Admins can update notices" on notices;
create policy "Admins can update notices"
  on notices for update using ( auth.role() = 'authenticated' );


-- C. STORAGE BUCKETS

insert into storage.buckets (id, name, public)
values ('notes', 'notes', true)
on conflict (id) do nothing;

-- Storage Policies
drop policy if exists "Public Access" on storage.objects;
create policy "Public Access"
  on storage.objects for select using ( bucket_id = 'notes' );

drop policy if exists "Auth Upload Access" on storage.objects;
create policy "Auth Upload Access"
  on storage.objects for insert with check ( bucket_id = 'notes' and auth.role() = 'authenticated' );

drop policy if exists "Auth Delete Access" on storage.objects;
create policy "Auth Delete Access"
  on storage.objects for delete
  using ( bucket_id = 'notes' and auth.role() = 'authenticated' );


-- D. INITIAL DATA
insert into notices (id, content, is_active)
values (1, 'Welcome to NoteShare! Exam updates will appear here.', true)
on conflict (id) do nothing;

notify pgrst, 'reload schema';
